public class CalculadoraCastroMarc {
	public int sumar(int operand1, int operand2) {
	     return operand1 + operand2;
	  }

	  public int restar(int operand1, int operand2) {
	     return operand1 - operand2;
	  }

	  public int multiplicar(int operand1, int operand2) {
	     return operand1 * operand2;
	  }      

	  public int dividir(int operand1, int operand2) {
	     return operand1 / operand2;
	  }
}
